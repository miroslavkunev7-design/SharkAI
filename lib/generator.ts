function slugify(text: string) {
  return text
    .toLowerCase()
    .replace(/[^a-z0-9а-яё\s-]/gi, '')
    .trim()
    .replace(/\s+/g, '-')
    .slice(0, 40) || 'project';
}

function extractTitle(prompt: string) {
  const first = prompt.split(/[.!?\n]/)[0]?.trim();
  return first && first.length > 3 ? first.slice(0, 60) : 'My Application';
}

export function generateProjectFiles(
  type: string,
  prompt: string,
  projectName: string
) {
  const title = projectName || extractTitle(prompt);
  const slug = slugify(title);
  const desc = prompt || 'Generated by SharkAI';

  const sharedReadme = `# ${title}

> Generated by SharkAI Autonomous AI Platform

## Description
${desc}

## Type
${type}

## Quick Start
\`\`\`bash
npm install
npm run dev
\`\`\`

## Stack
- Frontend: Next.js / React / Tailwind CSS
- Backend: Node.js API
- Database: PostgreSQL + Prisma
- Deploy: Docker + Vercel
`;

  if (type === 'website' || type === 'saas') {
    return [
      { path: 'package.json', content: JSON.stringify({
        name: slug,
        version: '1.0.0',
        private: true,
        scripts: { dev: 'npx serve public -p 3000', start: 'npx serve public -p 3000' },
        description: desc.slice(0, 120),
      }, null, 2) },
      { path: 'public/index.html', content: buildWebsiteHtml(title, desc, type === 'saas') },
      { path: 'public/styles.css', content: buildWebsiteCss() },
      { path: 'public/app.js', content: buildWebsiteJs(title) },
      { path: 'README.md', content: sharedReadme },
      { path: 'Dockerfile', content: `FROM nginx:alpine\nCOPY public /usr/share/nginx/html\nEXPOSE 80\n` },
    ];
  }

  if (type === 'api') {
    return [
      { path: 'package.json', content: JSON.stringify({
        name: slug, version: '1.0.0', type: 'module',
        scripts: { start: 'node src/server.js', dev: 'node --watch src/server.js' },
        dependencies: { express: '^4.21.0', cors: '^2.8.5' },
      }, null, 2) },
      { path: 'src/server.js', content: buildApiServer(title, desc) },
      { path: 'README.md', content: sharedReadme },
    ];
  }

  if (type === 'database') {
    return [
      { path: 'schema.prisma', content: buildPrismaSchema(slug, desc) },
      { path: 'seed.sql', content: buildSeedSql(title) },
      { path: 'README.md', content: sharedReadme },
    ];
  }

  if (type === 'mobile') {
    return [
      { path: 'pubspec.yaml', content: `name: ${slug.replace(/-/g, '_')}\ndescription: ${desc.slice(0, 80)}\nversion: 1.0.0\nenvironment:\n  sdk: '>=3.0.0 <4.0.0'\ndependencies:\n  flutter:\n    sdk: flutter\n` },
      { path: 'lib/main.dart', content: buildFlutterMain(title, desc) },
      { path: 'README.md', content: sharedReadme },
    ];
  }

  if (type === 'desktop') {
    return [
      { path: 'package.json', content: JSON.stringify({
        name: slug, version: '1.0.0', main: 'main.js',
        scripts: { start: 'electron .' },
        devDependencies: { electron: '^33.0.0' },
      }, null, 2) },
      { path: 'main.js', content: buildElectronMain(title) },
      { path: 'index.html', content: buildWebsiteHtml(title, desc, false) },
      { path: 'README.md', content: sharedReadme },
    ];
  }

  if (type === 'game') {
    return [
      { path: 'index.html', content: buildGameHtml(title, desc) },
      { path: 'game.js', content: buildGameJs() },
      { path: 'README.md', content: sharedReadme },
    ];
  }

  return [
    { path: 'package.json', content: JSON.stringify({ name: slug, version: '1.0.0' }, null, 2) },
    { path: 'src/index.ts', content: `// SharkAI Generated\n// ${desc}\n\nexport function main() {\n  console.log("${title}");\n}\n\nmain();\n` },
    { path: 'README.md', content: sharedReadme },
  ];
}

