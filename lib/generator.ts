function buildGameJs() {
  return `const c=document.getElementById('game'),x=c.getContext('2d');
let px=240,py=160,dx=3,dy=2;
function loop(){x.fillStyle='#0a0e1a';x.fillRect(0,0,480,320);x.fillStyle='#00D2FF';x.beginPath();x.arc(px,py,12,0,Math.PI*2);x.fill();
px+=dx;py+=dy;if(px<12||px>468)dx=-dx;if(py<12||py>308)dy=-dy;requestAnimationFrame(loop)}loop();`;
}
