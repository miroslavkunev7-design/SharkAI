function buildWebsiteJs(title: string) {
  return `document.addEventListener('DOMContentLoaded',()=>{console.log('${title.replace(/'/g, "\\'")} loaded');const b=document.querySelector('.btn');if(b)b.addEventListener('click',e=>{e.preventDefault();alert('Welcome to ${title.replace(/'/g, "\\'")}!')})});`;
}

function buildWebsiteHtml(title: string, desc: string, isSaas: boolean) {
  const features = isSaas
    ? ['Dashboard Analytics', 'User Management', 'Subscription Billing', 'API Integration']
    : ['Modern Design', 'Mobile Responsive', 'Fast Performance', 'SEO Optimized'];

  const featureCards = features
    .map((f) => `<div class="card"><h3>${f}</h3><p>Built automatically by SharkAI multi-agent system.</p></div>`)
    .join('\n        ');

  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${title}</title>
  <link rel="stylesheet" href="styles.css">
</head>
<body>
  <section class="hero">
    <h1>${title}</h1>
    <p>${desc.slice(0, 200)}</p>
    <a href="#" class="btn">${isSaas ? 'Start Free Trial' : 'Get Started'}</a>
  </section>
  <section class="features">
    ${featureCards}
  </section>
  <footer>Generated by SharkAI · ${new Date().getFullYear()}</footer>
  <script src="app.js"></script>
</body>
</html>`;
}

function buildApiServer(title: string, desc: string) {
  return `import express from 'express';
import cors from 'cors';

const app = express();
app.use(cors());
app.use(express.json());

const items = [
  { id: 1, name: 'Sample Item', description: '${desc.slice(0, 80).replace(/'/g, "\\'")}' },
];

app.get('/api/health', (_, res) => res.json({ status: 'ok', service: '${title.replace(/'/g, "\\'")}' }));
app.get('/api/items', (_, res) => res.json(items));
app.post('/api/items', (req, res) => {
  const item = { id: items.length + 1, ...req.body };
  items.push(item);
  res.status(201).json(item);
});

const PORT = process.env.PORT || 4000;
app.listen(PORT, () => console.log('${title} API running on port ' + PORT));
`;
}

function buildPrismaSchema(slug: string, desc: string) {
  return `generator client { provider = "prisma-client-js" }
datasource db { provider = "postgresql" url = env("DATABASE_URL") }

// ${desc.slice(0, 100)}
model User {
  id        String   @id @default(cuid())
  email     String   @unique
  name      String?
  createdAt DateTime @default(now())
  posts     Post[]
}

model Post {
  id        String   @id @default(cuid())
  title     String
  content   String?
  authorId  String
  author    User     @relation(fields: [authorId], references: [id])
  createdAt DateTime @default(now())
}
`;
}

